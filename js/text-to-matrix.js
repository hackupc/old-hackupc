exports.textToMatrix = function textToMatrix (text, size, bg, fill) {
  var sizec = size.split('x').map(Number)
  var width = sizec[0]
  var height = sizec[1]
  var result = new Array(height)
  for (var i = 0; i < height; ++i) {
    result[i] = []
  }
  var firstLetter = true
  Array.prototype.forEach.call(text, function (c) {
    var letterMatrix = exports[size][c]
    if (letterMatrix == null) {
      throw new Error('Missing letter "' + c + '" in dict size ' + size)
    }
    for (var i = 0; i < height; ++i) {
      if (!firstLetter) {
        result[i].push(bg)
      }
      result[i] = result[i].concat(letterMatrix[i])
    }
    firstLetter = false
  })
  for (var i = 0; i < height; ++i) {
    var row = result[i]
    for (var j = 0; j < row.length; ++j) {
      var p = row[j]
      if (p === 0) {
        p = bg
      } else {
        p = fill
      }
      row[j] = p
    }
  }
  return result
}

